$(function(){
	$('#language-sel').change(function() {
		
		var lang = document.getElementById("language-sel").value;

		$('.menu--label').each(function(index, item){
			$(this).text(ArrLang_Menu[lang][$(this).attr('key')]);
		});

		$('.content_cust').each(function(index, item){
			$(this).text(ArrCust[lang][$(this).attr('key')]);
		});

		if (lang == "ru") {
			localStorage.setItem("language", "russian");
			$("#language-sel").val('ru'); 
		}
		else if(lang == "en"){
			localStorage.setItem("language", "english");
			$("#language-sel").val('en');
		}
	});
});

